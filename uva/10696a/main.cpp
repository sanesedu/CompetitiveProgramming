using namespace std;

long long f91(long long n) {
    
    if(n >= 101) return n - 10;
    else {
        return f91(f91(n+11));
    }
}

int main() {

    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    cout.precision(10);

    long long n;

    while(cin >> n) {
        if(n == 0) break;
        
        cout << "f91(" << n << ") = " << f91(n) << endl;    
    }

    return 0;
}
